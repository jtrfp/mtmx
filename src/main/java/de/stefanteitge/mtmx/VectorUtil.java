package de.stefanteitge.mtmx;

import org.lwjgl.util.vector.Vector3f;


public final class VectorUtil {

	private VectorUtil() {
	}

	public static Vector3f sub(Vector3f a, Vector3f b) {
		Vector3f r = new Vector3f();
		r.x = a.x - b.x;
		r.y = a.y - b.y;
		r.z = a.z - b.z;
		return r;
	}


	public static Vector3f normal(Vector3f a, Vector3f b) {
		Vector3f r = new Vector3f();
		r.x = a.y * b.z - a.z * b.y;
		r.y = a.z * b.x - a.x * b.z;
		r.z = a.x * b.y - a.y * b.x;
		return r;
	}
}
